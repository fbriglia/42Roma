int	ft_atoi(char *str)
{
	int	i;
	int	result;
	int	sign;

	i = 0;
	result = 0;
	sign = 1;
	while (str[i] >= 9 && str[i] <= 13 || str[i] == 32 || str[i] == '+')
		i++;
	while (str[i] == '-')
	{
		if (str[i++] == '-')
			sign = sign * (-1);
	}
	while (str[i] >= '0' && str[i] <= '9')
		result = result * 10 + str[i++] - '0';
	return (result * sign);
}
